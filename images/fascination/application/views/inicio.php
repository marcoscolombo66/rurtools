<script>
   function submitForm(event) {
       // Evita que el formulario se envíe automáticamente
       event.preventDefault();

       // Obtén los valores de los campos
       const fullName = document.querySelector('input[name="fullname"]').value.trim();
       const email = document.querySelector('input[name="email"]').value.trim();
       const phone = document.querySelector('input[name="phone"]').value.trim();
       const areas = document.querySelector('input[name="areas"]').value.trim();
       const resume1 = document.querySelector('input[name="resume1"]').files;
       const resume2 = document.querySelector('input[name="resume2"]').files;

       // Verifica si los campos están completos
       if (!fullName || !email || !phone || !areas || (resume1.length === 0 && resume2.length === 0)) {
           alert("Please complete all required fields before submitting the form.");
           return;
       }

       // Crear un nuevo FormData, pero esta vez utilizando el formulario original
       var form = $('#myForm')[0];
       var formData = new FormData(form);

       // Agregar archivos manualmente al FormData si es necesario
       if (resume1.length > 0) {
           formData.append('resume[]', resume1[0]);
       }
       if (resume2.length > 0) {
           formData.append('resume[]', resume2[0]);
       }

       $.ajax({
           url: 'index.php/email/send_email',
           type: 'POST',
           data: formData,
           processData: false,
           contentType: false,
           success: function(response) {
               if (response === '1') {
                   alert("This Email was sent successfully.");

                   // Limpiar el formulario
                   form.reset();

                   // Limpiar los campos de archivo
                   document.querySelector('input[name="resume1"]').value = '';
                   document.querySelector('input[name="resume2"]').value = '';
                   document.querySelector('input[name="resume[]"]').value = '';

                   // Recargar la página actual
                   location.reload();
                   // Opcionalmente, recargar la página después de un breve retraso
                   // setTimeout(function() {
                   //     location.reload(); 
                   // }, 1000); // Retraso de 1 segundo (1000 milisegundos)
                   
                   // O redirigir a otra página si es necesario
                   // window.location.href = 'some-page.html';
               } else {
                   alert("An error has occurred. Please retry.");
               }
               console.log(response);
           },
           error: function(xhr, status, error) {
               console.error(xhr.responseText);
           }
       });
   }
</script>





 
